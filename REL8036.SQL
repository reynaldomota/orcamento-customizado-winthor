SELECT
  PCFILIAL.LOGOMARCA,
  PCFILIAL.RAZAOSOCIAL,
  PCFILIAL.ENDERECO,
  PCFILIAL.CIDADE,
  PCFILIAL.UF,
  PCFILIAL.CEP,
  PCFILIAL.CGC,
  PCFILIAL.TELEFONE,
  PCUSUARI.NOME,
  o.NUMORCA,
  PCORCAVENDAI.DATA,
  PCORCAVENDAI.CODCLI,
  PCORCAVENDAI.CODUSUR,
  PCORCAVENDAI.QT,
  PCORCAVENDAI.PVENDA,
  PCORCAVENDAI.PTABELA,
  PCFILIAL.CODIGO,
  PCPRODUT.DESCRICAO,
  PCPRODUT.EMBALAGEM,
  PCPRODUT.UNIDADE,
  PCPRODUT.PESOLIQ,
  PCPRODUT.PESOBRUTO,
  PCPRODUT.QTUNIT,
  PCCLIENT.CLIENTE,
  PCUSUARI.EMAIL,
  PCPRODUT.CODPROD,
  PCCOB.COBRANCA,
  PCCOB.PAGCOMISSAO,
  PCCLIENT.CODCLI,
  PCCLIENT.ENDERENT,
  PCCLIENT.BAIRROENT,
  PCCLIENT.TELENT,
  PCCLIENT.MUNICENT,
  PCCLIENT.ESTENT,
  PCCLIENT.CEPENT,
  PCCLIENT.CGCENT,
  PCCLIENT.IEENT,
  PCPRODUT.CODNCMEX,
  PCPRODUT.NUMORIGINAL,
  o.DATA AS DATA1,
  o.VLTOTAL,
  o.CODCLI AS CODCLI1,
  o.CODUSUR AS CODUSUR1,
  o.DTENTREGA,
  o.VLTABELA,
  o.CODFILIAL,
  o.VLDESCONTO,
  o.TIPOVENDA,
  o.NUMITENS,
  o.CODEMITENTE,
  o.CODCOB,
  PCORCAVENDAI.QT * PCORCAVENDAI.PVENDA AS clVLTOTAL,
  o.DTVALIDADE,
  o.VLOUTRASDESP,
  o.VLFRETE,
  o.PLPAGAVISTA,
  PCPLPAG.DESCRICAO AS PLPAGAMENTO,
  PCPLPAG.NUMDIAS,
  o.VLOUTRASDESP + o.VLFRETE AS VLATEND,
  PCFILIAL.ENDERECOCOMP,
  PCUSUARI."ven_tel",
  PCUSUARI."ven_cel",
  PCUSUARI."ven_skype",
  PCUSUARI.OBSERVACAO,
  PCFILIAL.BAIRRO,
  PCFILIAL.NUMERO,
  PCPRODUT.INFORMACOESTECNICAS
FROM
  PCORCAVENDAC o
  INNER JOIN (SELECT FILIAL.CODIGO,
    FILIAL.LOGOMARCA,
    FILIAL.RAZAOSOCIAL,
    FILIAL.ENDERECO,
    FILIAL.CIDADE,
    FILIAL.UF,
    FILIAL.CEP,
    FILIAL.CGC,
    FILIAL.TELEFONE,
    FILIAL.ENDERECOCOMP,
    FILIAL.BAIRRO,
    FILIAL.NUMERO FROM PCFILIAL FILIAL) PCFILIAL ON o.CODFILIAL = PCFILIAL.CODIGO
  INNER JOIN (SELECT PCORCAVENDAI.DATA,
    PCORCAVENDAI.CODCLI,
    PCORCAVENDAI.CODPROD,
    PCORCAVENDAI.CODUSUR,
    PCORCAVENDAI.QT,
    PCORCAVENDAI.PVENDA,
    PCORCAVENDAI.PTABELA,
    PCORCAVENDAI.NUMORCA FROM PCORCAVENDAI) PCORCAVENDAI ON o.NUMORCA = PCORCAVENDAI.NUMORCA
  INNER JOIN (SELECT PCPRODUT.CODPROD,
    PCPRODUT.DESCRICAO,
    PCPRODUT.EMBALAGEM,
    PCPRODUT.UNIDADE,
    PCPRODUT.PESOLIQ,
    PCPRODUT.PESOBRUTO,
    PCPRODUT.QTUNIT,
    PCPRODUT.CODNCMEX,
    PCPRODUT.NUMORIGINAL,
    PCPRODUT.INFORMACOESTECNICAS FROM PCPRODUT) PCPRODUT ON PCORCAVENDAI.CODPROD = PCPRODUT.CODPROD
  INNER JOIN (SELECT PCCLIENT.CLIENTE,
    PCCLIENT.CODCLI,
    PCCLIENT.ENDERENT,
    PCCLIENT.BAIRROENT,
    PCCLIENT.TELENT,
    PCCLIENT.MUNICENT,
    PCCLIENT.ESTENT,
    PCCLIENT.CEPENT,
    PCCLIENT.CGCENT,
    PCCLIENT.IEENT FROM PCCLIENT) PCCLIENT ON PCCLIENT.CODCLI = o.CODCLI
  INNER JOIN (SELECT PCEMPR.NOME,
    PCEMPR.EMAIL,
    PCEMPR.OBSERVACAO,
    PCEMPR.CODUSUR,
    PCEMPR.FONE AS "ven_tel",
    PCEMPR.CELULAR AS "ven_cel",
    PCEMPR.NOMEEMAIL AS "ven_skype" FROM PCEMPR) PCUSUARI ON PCUSUARI.CODUSUR = o.CODUSUR
  INNER JOIN (SELECT PCCOB.COBRANCA,
    PCCOB.PAGCOMISSAO,
    PCCOB.CODCOB FROM PCCOB) PCCOB ON o.CODCOB = PCCOB.CODCOB
  INNER JOIN (SELECT PCPLPAG.CODPLPAG,
    PCPLPAG.DESCRICAO,
    PCPLPAG.NUMDIAS,
    PCPLPAG.TIPOPRAZO FROM PCPLPAG) PCPLPAG ON o.CODPLPAG = PCPLPAG.CODPLPAG
WHERE
  o.NUMORCA = :NUMORCA

